#slicing /selecting subsequences
lang = "python"
#syntax - [start arguement: stop arguement:step]
print("Amanjeet"[2:5:1])   #output= anj
print("Amanjeet"[2:5])     #output= anj
print("Amanjeet"[2:5:2])   #output= aj 
print("Amanjeet"[0::2])    #output= Aaje
print("Amanjeet"[8::-1])   #output = teejnamA
print("Amanjeet"[::-1])    #output = teejnamA (TRICK)